CREATE DATABASE Db_Template;
GO
USE Db_Template;
go

CREATE TABLE rols(
	id INT PRIMARY KEY IDENTITY(1,1),
	name VARCHAR(10),
	description VARCHAR(15)
	);

CREATE TABLE accounts(
	id UNIQUEIDENTIFIER PRIMARY KEY DEFAULT NEWID(),
	acount VARCHAR(30),
	pount VARCHAR(30),
	roleId INT,
	FOREIGN KEY (roleId) REFERENCES rols(id) ON UPDATE CASCADE ON DELETE CASCADE
	);

CREATE TABLE users(
	id UNIQUEIDENTIFIER PRIMARY KEY DEFAULT NEWID(),
	curp VARCHAR(20),
	accountId UNIQUEIDENTIFIER NOT NULL,
	name VARCHAR(30),
	lastName VARCHAR(30),
	secondLastName VARCHAR(30),
	salary FLOAT,
	phone VARCHAR(14),
	state BIT DEFAULT 1,
	FOREIGN KEY (accountId) REFERENCES accounts(id) ON UPDATE CASCADE ON DELETE CASCADE
	);
